function potencia(a, b) {
  return Math.pow(a, b)
}

console.log(potencia(3, 2));
console.log(potencia(7, 2));
console.log(potencia(7, 3));
console.log(potencia(2, 10));

function potenciaResolucao(a, b) {
  return a ** b
}

console.log(potencia(3, 2));
console.log(potencia(7, 2));
console.log(potencia(7, 3));
console.log(potencia(2, 10));