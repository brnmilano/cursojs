//Comparações Booleans

console.log(5 > 2); // 5 é maior que 2?
console.log(3 < 10); // 3 é menor que 10?
console.log(3 >= 3); // 3 é maior ou igual a 3?
console.log(5 <= 4); // 5 é menor ou igual a 4?
console.log(5 == 4); // 5 é igual a 4?
console.log("Mateus" != "Mateus"); // é diferente?
console.log(3 === '3'); // é idêntico?

